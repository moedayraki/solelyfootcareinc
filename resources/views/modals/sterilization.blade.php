        Your safety is our TOP priority and therefore we have high standards for infection control. All equipment used is sterilized after each use or one time use instruments are discarded after care.  Our sterilization procedure is as follows:
        <br />
        <br /><b>.</b>We start by using a cleaning solution to clean away any visible debris
        <br /><b>.</b>We use an ultrasonic cleaner so ensure effective cleaning within all crevices.
        <br /><b>.</b>We then package all our instruments in sterile pouches that contain a chemical indicator both on the package and inside the pouch.
        <br /><b>.</b>We then place these packaged instruments into our steam autoclave.
        <br /><b>.</b>Our autoclave reaches a heat of 273 degrees each cycle. This temperature ensures that all bacteria, spores, fungi and any other microorganisms have been killed.
        <br /><b>.</b>The autoclave cycle is confirmed by a receipt like print out stating the temperature the autoclaved reached to complete sterilization. The receipt load number is labeled on each pouch from that cycle and documented into our sterilization log book for reference if needed.
        <br /><b>.</b>This entire process is Public Health Approved

